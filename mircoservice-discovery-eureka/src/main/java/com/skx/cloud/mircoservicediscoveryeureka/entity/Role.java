package com.skx.cloud.mircoservicediscoveryeureka.entity;


/**
 * 角色类
 */
public class Role {

    private int id;   //角色 Id

    private String name;   //角色名


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
